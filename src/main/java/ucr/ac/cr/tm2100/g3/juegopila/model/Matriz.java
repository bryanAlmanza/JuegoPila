    //metodo que verifica el valor de la matriz, si el valor es 0, entonces hay camino por el cual caminar
    public boolean esCamino(int x, int y) {
        return x >= 0 && y >= 0 && x < getColumnas() && y < getFilas() && matriz[y][x] == 0;
    }

    //metodo que verifica los valores de la matriz, si es menor a 0, esta fuera de la matriz
    public int getValor(int x, int y) {
        if (x >= 0 && y >= 0 && x < getColumnas() && y < getFilas()) {
            return matriz[y][x];
        }
        return -1; // Valor inválido si está fuera de la matriz
    }

